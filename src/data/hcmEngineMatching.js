// 按齿轮箱型号查询配机案例
export function getMatchingCasesByGearbox(gearboxModel) {
  if (!gearboxModel) return [];
  const modelUpper = gearboxModel.toUpperCase();
  return engineMatchingCases.filter(c =>
    c.gearbox && c.gearbox.toUpperCase().includes(modelUpper)
  );
}

// 按主机型号查询配机案例
export function getMatchingCasesByEngine(engineModel) {
  if (!engineModel) return [];
  const modelLower = engineModel.toLowerCase();
  return engineMatchingCases.filter(c =>
    (c.engine && c.engine.toLowerCase().includes(modelLower)) ||
    (c.engineModel && c.engineModel.toLowerCase().includes(modelLower))
  );
}

