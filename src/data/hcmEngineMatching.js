// 辅助函数：根据主机型号识别品牌
export function identifyEngineBrand(engineModel) {
  if (!engineModel) return 'other';
  const model = engineModel.toLowerCase();

  if (model.includes('康明斯') || model.includes('cummins') || model.includes('k19') ||
      model.includes('k38') || model.includes('k50') || model.includes('kta') ||
      model.includes('qsnt') || model.includes('qsd') || model.includes('qsz') ||
      model.includes('qsl')) {
    return 'cummins';
  }
  if (model.includes('潍柴') || model.includes('weichai') || model.includes('wp13') ||
      model.includes('wp17') || model.includes('wp10') || model.includes('8m21') ||
      model.includes('16m45') || model.includes('6m21')) {
    return 'weichai';
  }
  if (model.includes('玉柴') || model.includes('yuchai') || model.includes('yc6')) {
    return 'yuchai';
  }
  if (model.includes('沃尔沃') || model.includes('volvo') || model.includes('d6') ||
      model.includes('d8')) {
    return 'volvo';
  }
  if (model.includes('河柴') || model.includes('chd')) {
    return 'hechai';
  }
  if (model.includes('航发') || model.includes('杭汽发') || model.includes('mc13')) {
    return 'hangfa';
  }
  if (model.includes('iveco') || model.includes('fpf')) {
    return 'iveco';
  }
  return 'other';
}

// 辅助函数：根据船型关键字识别船舶类型
export function identifyShipType(shipTypeStr) {
  if (!shipTypeStr) return 'other';
  const str = shipTypeStr.toLowerCase();

  for (const type of shipTypes) {
    for (const keyword of type.keywords) {
      if (str.includes(keyword.toLowerCase())) {
        return type.id;
      }
    }
  }
  return 'other';
}

