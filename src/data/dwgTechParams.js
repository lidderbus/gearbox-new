// 按系列获取型号
export const getModelsBySeries = (series) => {
  return Object.values(dwgTechParams).filter(item => item.series === series);
};

// 获取已匹配型号
export const getMatchedModels = () => {
  return Object.values(dwgTechParams).filter(item => item.matchStatus === 'matched');
};

// 获取未匹配型号
export const getUnmatchedModels = () => {
  return Object.values(dwgTechParams).filter(item => item.matchStatus === 'unmatched');
};

// 获取所有齿轮箱
export const getAllGearboxes = () => {
  return Object.values(dwgTechParams).filter(item => item.type === 'gearbox');
};

// 获取所有联轴器
export const getAllCouplings = () => {
  return Object.values(dwgTechParams).filter(item => item.type === 'coupling');
};

// 搜索型号
export const searchModels = (keyword) => {
  const key = keyword.toUpperCase();
  return Object.values(dwgTechParams).filter(item =>
    item.model.toUpperCase().includes(key) ||
    item.series?.toUpperCase().includes(key)
  );
};

// 获取系列列表
export const getSeriesList = () => {
  const series = new Set();
  Object.values(dwgTechParams).forEach(item => {
    if (item.series) series.add(item.series);
  });
  return Array.from(series).sort();
};

